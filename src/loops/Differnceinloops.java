package loops;

public class Differnceinloops {
public static void main(String[] args) {
	System.out.println("For loop Execution :");
	for(int i=0;i<3;i++) {
		System.out.println("Hello World Using For Loop");
	}
	System.out.println("While loop Execution");
	int i=0;
	while(i<3) {
		System.out.println("Hello World in While Loop");
		i++;
	}
	
	System.out.println("Do While Loop Execution");
	i=0;
	do {
		System.out.println("Hello World in Do While Loop ");
		i++;
	}while(i<3);
	
}
}
